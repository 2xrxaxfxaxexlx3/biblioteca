    void listar(){
        String sql = "SELECT d.id_devolucion,d.fecha_d, d.hora_d, c.codigo, c.nombre_cliente, c.apellido_cliente, d.estado from devolucion d\n" +
                     "join prestamo p on d.id_prestamo = p.id_prestamo\n" +
                     "join cliente c on p.codigo = c.codigo;";
        try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] dev = new Object[7];
            model = (DefaultTableModel)tabladatos.getModel();
            while(rs.next()){
                dev[0]= rs.getString("id_devolucion");
                dev[1]= rs.getString("fecha_d");
                dev[2]=rs.getString("hora_d");
                dev[3]=rs.getString("codigo");
                dev[4]=rs.getString("nombre_cliente");
                dev[5]=rs.getString("apellido_cliente");
                dev[6]=rs.getString("estado");
                model.addRow(dev);
            }
            tabladatos.setModel(model);
        } catch (Exception e) {
        }
    }
    void modificar(){
        String estado = "";
        if(rb_pendiente.isSelected()){
            estado = "pendiente";
        }
        if(rb_realizado.isSelected()){
            estado = "realizado";
        }
        if(rb_atrasado.isSelected()){
            estado = "atrasado";
        }
        String sql = "Update devolucion set estado ='"+estado+"' where id_devolucion ="+ txt_id.getText();
        try {
            con = cn.getConection();
            st = con.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Usuario Modificado");
            limpiarTabla(model);
        } catch (Exception e) {
        }
    }
    void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i <= tabladatos.getRowCount()-1; i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }

