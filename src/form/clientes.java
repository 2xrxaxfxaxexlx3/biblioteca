    void listar(){
        String sql ="select c.codigo,c.dni_cliente,nombre_cliente,apellido_cliente,c.tipo from cliente c";
        try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] cliente = new Object[5];
            model = (DefaultTableModel) tabladatos.getModel();
            while(rs.next()){
                cliente[0]= rs.getString("codigo");
                cliente[1]= rs.getString("dni_cliente");
                cliente[2]= rs.getString("nombre_cliente");
                cliente[3]= rs.getString("apellido_cliente");
                cliente[4]= rs.getString("tipo");
                model.addRow(cliente);
            }
            tabladatos.setModel(model);
        } catch (Exception e) {
        }
    }
